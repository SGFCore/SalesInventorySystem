-- =============================================
-- 34. DISCOUNT TABLE
-- =============================================
CREATE TABLE DISCOUNT (
    DiscountID        NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
    CustomerTypeID    NUMBER,
    DiscountName      NVARCHAR2(100) NOT NULL,
    Value             NUMBER NOT NULL,
    Detail            VARCHAR2(300) NOT NULL,
    AppliedProductIDs VARCHAR2(4000),
    Status            NUMBER DEFAULT 2,
    ExpiryDate        DATE NOT NULL,
    StartDate         DATE NOT NULL,
    CONSTRAINT fk_discount_customertype FOREIGN KEY (CustomerTypeID) REFERENCES CUSTOMERTYPE(CustomerTypeID),
    CONSTRAINT chk_discount_dates CHECK (ExpiryDate > StartDate)
);

-- =============================================
-- 35. LISTDISCOUNT TABLE (references Order, Discount)
-- =============================================
CREATE TABLE LISTDISCOUNT (
    OrderID      NUMBER,
    DiscountID   NUMBER,
    AppliedValue NUMBER NOT NULL,
    PRIMARY KEY (OrderID, DiscountID),
    CONSTRAINT fk_ld_order FOREIGN KEY (OrderID) REFERENCES "ORDER"(OrderID),
    CONSTRAINT fk_ld_discount FOREIGN KEY (DiscountID) REFERENCES DISCOUNT(DiscountID)
);

-- =============================================
-- 36. FEEDBACK TABLE (references OrderDetail, Customer)
-- =============================================
CREATE TABLE FEEDBACK (
    FeedbackID      NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
    OrderDetailID   NUMBER,
    CustomerID      NUMBER,
    FeedbackComment VARCHAR2(200) NOT NULL,
    FeedBackDate    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    AttachmentURL   VARCHAR2(4000),
    Rating          NUMBER NOT NULL,
    CONSTRAINT fk_feedback_orderdetail FOREIGN KEY (OrderDetailID) REFERENCES ORDERDETAIL(OrderDetailID),
    CONSTRAINT fk_feedback_customer FOREIGN KEY (CustomerID) REFERENCES CUSTOMER(CustomerID)
);
